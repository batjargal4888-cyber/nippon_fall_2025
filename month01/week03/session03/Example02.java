class Example02 {
    public static void main(String[] args){
        // Arithmetic operation => Тоон дээр
        int a = 6;
        int b = 7;
        // нэмэх ==> +
        int addResult = a + b;
        System.out.println("2 тоог нэмэх: " + addResult);
        // хасах ==> -
        int minusResult = a - b;
        System.out.println("2 тоог хасах: " + minusResult);
        // үржүүлэх ==> *
        int multiplyResult = a * b;
        System.out.println("2 тоог үржүүлэх: " + multiplyResult);
        // хуваах ==> /
        int divisionResult = a / b;
        System.out.println("2 тоог хуваах: " + divisionResult);
        // үлдэлтэй хуваах ==> %
        int moduleResult = a % b;
        System.out.println("2 тоог үлдэгдэлтэй хуваах: " + moduloResult);

    }
}